const month = 3

switch (month) {
    case 1:
        console.log('January');
        break;
    case 2:
        console.log('February');
        break;
    case 3:
        console.log('March');
        break;

    default:
        console.log('Not in jan. feb and march');
        
        break;
} // if break is not there then cases after the matched cases will execute automatically expect default.

